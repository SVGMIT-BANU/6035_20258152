const supportedRoutes = ["/farmer-dashboard", "/farmer-dashboard/profile", "/farmer-dashboard/products", "/farmer-dashboard/orders", "/farmer-dashboard/reports", "/farmer-dashboard/complaints", "/price-prediction"];

const normalizePath = (pathname: string) => {
  return supportedRoutes.find((route) => pathname === route) ?? "default";
};

const FarmerVoiceAgent = () => {
  const location = useLocation();
  const { language } = useLanguage();
  const { supported, speaking, speak, stop } = useSpeechGuide(language);
  const [collapsed, setCollapsed] = useState(false);
  const [autoSpeak, setAutoSpeak] = useState(true);
  const user = JSON.parse(localStorage.getItem("user") || "{}");

  const activeRoute = normalizePath(location.pathname);
  const copy = routeCopy[activeRoute]?.[language] ?? routeCopy.default[language];
  const message = `${copy.intro} ${copy.currentStep} ${copy.help}`;

  useEffect(() => {
    if (!autoSpeak || !supported || user?.user_type !== "farmer") return;
    speak(message);
  }, [location.pathname, language, autoSpeak, supported]);

  if (user?.user_type !== "farmer") {
    return null;
  }

  return (
    <div className="fixed bottom-4 right-4 z-50 w-[320px] max-w-[calc(100vw-2rem)]">
      {collapsed ? (
        <Button className="w-full shadow-lg" onClick={() => setCollapsed(false)}>
          <Bot className="h-4 w-4 mr-2" />
          {copy.show}
        </Button>
      ) : (
        <Card className="shadow-lg border-primary/20">
          <CardHeader className="pb-3">
            <div className="flex items-start justify-between gap-3">
              <div className="flex items-center gap-2">
                <Bot className="h-5 w-5 text-primary" />
                <CardTitle className="text-base">{copy.title}</CardTitle>
              </div>
              <Button variant="ghost" size="icon" className="h-8 w-8" onClick={() => setCollapsed(true)}>
                <Minimize2 className="h-4 w-4" />
              </Button>
            </div>
          </CardHeader>
          <CardContent className="space-y-4">
            <div className="rounded-md bg-muted/40 p-3 text-sm">
              <p className="font-medium">{copy.intro}</p>
              <p className="mt-2 text-muted-foreground">{copy.currentStep}</p>
            </div>
            <div className="grid grid-cols-2 gap-2">
              <Button type="button" variant="secondary" onClick={() => speak(message)} disabled={!supported}>
                <Volume2 className="h-4 w-4 mr-2" />
                {copy.repeat}
              </Button>
              <Button type="button" variant="outline" onClick={stop} disabled={!supported || !speaking}>
                <Square className="h-4 w-4 mr-2" />
                {copy.stop}
              </Button>
              <Button type="button" variant="outline" onClick={() => speak(copy.help)} disabled={!supported}>
                <CircleHelp className="h-4 w-4 mr-2" />
                {copy.moreHelp}
              </Button>
              <Button type="button" variant="outline" onClick={() => setAutoSpeak((value) => !value)}>
                {autoSpeak ? copy.hide : <><Maximize2 className="h-4 w-4 mr-2" />{copy.auto}</>}
              </Button>
            </div>
            {!supported ? (
              <p className="text-xs text-amber-700">
                {copy.unsupported}
              </p>
            ) : null}
          </CardContent>
        </Card>
      )}
    </div>
  );
};

export default FarmerVoiceAgent;
